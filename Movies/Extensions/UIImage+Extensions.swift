import UIKit

extension UIImage {
    class func homeTab() -> UIImage? {
        UIImage(named: "home")
    }
    
    class func ticketTab() -> UIImage? {
        UIImage(named: "ticket")
    }
    
    class func notificationsTab() -> UIImage? {
        UIImage(named: "notifications")
    }
    
    class func profileTab() -> UIImage? {
        UIImage(named: "profile")
    }
    
    class func searchIcon() -> UIImage? {
        UIImage(named: "search")
    }
    
    class func shareIcon() -> UIImage? {
        UIImage(named: "share")
    }
    
    class func playButtonIcon() -> UIImage? {
        UIImage(named: "playButton")
    }
}
